public class Departamento {
    private int id;
    private String nome;

    //Getters
    public int getId(int id){
        return this.id;
    }
    public String getNome(String nome){
        return this.nome;
    }

    //Setters
    public void setId(int id){
        this.id = id;
    }
    public void setNome(String nome){
        this.nome = nome;
    }
}
